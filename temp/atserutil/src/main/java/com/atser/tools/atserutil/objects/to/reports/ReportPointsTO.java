package com.atser.tools.atserutil.objects.to.reports;

/**
 *
 * @author droldan
 */
public class ReportPointsTO {

    private String x;
    private Long y;

    public ReportPointsTO() {
    }

    public ReportPointsTO(String x, Long y) {
        this.x = x;
        this.y = y;
    }

    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    public Long getY() {
        return y;
    }

    public void setY(Long y) {
        this.y = y;
    }
}
